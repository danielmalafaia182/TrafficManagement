CREATE SEQUENCE SEQ_USER
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;

CREATE TABLE TBL_USERS(
    ID_USER INTEGER DEFAULT SEQ_USER.NEXTVAL NOT NULL,
    EMAIL VARCHAR2(100) UNIQUE NOT NULL,
    PASSWORD VARCHAR2(100) NOT NULL,
    USER_ROLE VARCHAR2(50) DEFAULT 'USER'
);

